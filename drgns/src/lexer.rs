enum TokenType {
    // unambiguously single-character tokens
    SingleQuote,
    DoubleQuote,
    Caret,
    LeftParent,
    RightParent,
    LeftBracket,
    RightBracket,
    LeftBrace,
    RightBrace,
    Comma,
    Semicolon,
    Pipe,

    // one or more chars
    Plus,
    PlusPlus,
    PlusEquals,
    PlusPlusEquals,
    Mul,
    MulMul,
    MulEquals,
    MulMulEquals,
    Minus,
    MinusEquals,
    Arrow,
    Div,
    DivEquals,
    Mod,
    ModEquals,
    Less,
    LessEquals,
    DoubleLess,
    Greater,
    GreaterEquals,
    DoubleGreaterEquals,
    Colon,
    ColonColon,
    ColonEquals,
    Bang,
    BangEquals,
    Question,
    DoubleQuestion,
    DoubleQuestionEqual,
    Equal,
    EqualEqual,
    Dot,
    Ellipses,
    DollarBrace,

    // literals
    Identifier,
    Symbol,
    String,
    RawString,
    Integer,
    Float,

    // Keywords
    Underscore,
    True,
    False,
    None,
    And,
    Or,
    Xor,
    Not,
    Land,
    Lor,
    Lnot,
    Lxor,
    Lsl,
    Lsr,
    Rsr,
    Function,
    Return,
    Use,
    Mut,
    Continue,
    For,
    In,
    Move,
    Copy,
    Int,
    Float,
    Bool,
    Str,
    List,
    Obj,
    Sym,

    // whitespace, comments and already handled tokens
    Ignore,

    // unrecognized tokens
    Unknown,

    // a "fake" character emitted at the end of the stream
    EOF,
}

struct Token {
    token_type: TokenType,
}
